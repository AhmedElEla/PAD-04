package src.point;

public class Point {
    // Acces modifier X and Y
    private float x;
    private float y;
    // Constructor that initializes X and Y
    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
    // Getters for X and Y
    public float getX() {
        return x;
    }
    public float getY() {
        return y;
    }
}
